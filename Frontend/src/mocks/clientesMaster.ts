export function nextClienteCodigo(existing: Cliente[]): string {
  let max = 0
  for (const c of existing) {
    const m = /^CLI-(\d+)$/i.exec(c.codigo)
    if (m) max = Math.max(max, Number(m[1]))
  }
  return `CLI-${String(max + 1).padStart(6, '0')}`
}
